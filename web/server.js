app.post('/api/workflow/start', (req, res) => {
  const { task } = req.body;
  if (!task) return res.status(400).json({ error: 'Missing task' });
  const workflowId = Date.now().toString();
  const state = loadWorkflowState();
  state.current = { id: workflowId, task, stage: 'po', createdAt: new Date().toISOString() };
  state.pipelines = state.pipelines || [];
  state.pipelines.unshift(state.current);
  state.pipelines = state.pipelines.slice(0, 20);
  saveWorkflowState(state);

  sendTask('po', 'Dashboard', task, { workflowId, handoffTo: 'tech', taskId: workflowId });
  const ts = new Date().toISOString().replace('T', ' ').slice(0, 19);
  try { fs.appendFileSync(path.join(LOGS_DIR, 'team.log'),
        `[${ts}] [Dashboard] → PO (workflow): ${task.slice(0, 80)}…\n`); } catch {}

  res.json({ success: true, workflowId });
});

