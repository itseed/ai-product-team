// Parse VS Code's JSONC format (comments, trailing commas) safely
function parseJsonc(text) {
  const errors = [];
  const result = parse(text, errors, { allowTrailingComma: true });
  if (errors.length > 0) throw new Error(`JSONC parse error at offset ${errors[0].offset}`);
  return result;
}

function resolveFolder(folderPath, wsDir) {
  if (!folderPath) return null;
  const expanded = folderPath.startsWith('~')
    ? path.join(process.env.HOME || '', folderPath.slice(1))
    : folderPath;
  const abs = path.isAbsolute(expanded) ? expanded : path.resolve(wsDir, expanded);
  return { abs, exists: dirExists(abs) };
}

