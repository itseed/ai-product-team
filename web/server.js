// ── Paths config (runtime, mutable) ─────────────────────────

const DEFAULT_PATHS = {
  workspace:   path.join(SHARED_DIR, 'project', 'code'),
  projectRoot: path.join(SHARED_DIR, 'project'),
};

function loadPaths() {
  try {
    const raw = JSON.parse(fs.readFileSync(PATHS_FILE, 'utf8'));
    return {
      workspace:   raw.workspace   || DEFAULT_PATHS.workspace,
      projectRoot: raw.projectRoot || DEFAULT_PATHS.projectRoot,
    };
  } catch {
    // Ensure paths persist: create paths.json + paths.sh on first run
    ensurePathsPersisted();
    return { ...DEFAULT_PATHS };
  }
}

function ensurePathsPersisted() {
  try {
    const p = { ...DEFAULT_PATHS };
    fs.mkdirSync(path.dirname(PATHS_FILE), { recursive: true });
    fs.writeFileSync(PATHS_FILE, JSON.stringify(p, null, 2));
    const sh = [
      '# Auto-generated by dashboard — do not edit manually',
      `export WORKSPACE_DIR="${p.workspace}"`,
      `export PROJECT_ROOT="${p.projectRoot}"`,
    ].join('\n') + '\n';
    fs.writeFileSync(path.join(SHARED_DIR, 'paths.sh'), sh);
  } catch {}
}

function savePaths(updates) {
  const current = loadPaths();
  const next = { ...current, ...updates };

  // Resolve ~ to home dir
  for (const k of ['workspace', 'projectRoot']) {
    if (next[k] && next[k].startsWith('~')) {
      next[k] = path.join(process.env.HOME || '~', next[k].slice(1));
    }
    next[k] = path.resolve(next[k]);
  }

  fs.mkdirSync(path.dirname(PATHS_FILE), { recursive: true });
  fs.writeFileSync(PATHS_FILE, JSON.stringify(next, null, 2));

  // Also write shared/paths.sh so agent scripts can source it
  const sh = [
    '# Auto-generated by dashboard — do not edit manually',
    `export WORKSPACE_DIR="${next.workspace}"`,
    `export PROJECT_ROOT="${next.projectRoot}"`,
  ].join('\n') + '\n';
  fs.writeFileSync(path.join(SHARED_DIR, 'paths.sh'), sh);

  return next;
}

