// SSE — live log stream + heartbeat
app.get('/api/logs/stream', (req, res) => {
  res.setHeader('Content-Type',                'text/event-stream');
  res.setHeader('Cache-Control',               'no-cache');
  res.setHeader('Connection',                  'keep-alive');
  res.setHeader('Access-Control-Allow-Origin', '*');

  const logFile = path.join(LOGS_DIR, 'team.log');
  let lastSize = 0;

  try {
    const content = fs.readFileSync(logFile, 'utf8');
    lastSize = Buffer.byteLength(content, 'utf8');
    content.split('\n').filter(Boolean).slice(-25)
           .forEach(l => res.write(`data: ${JSON.stringify({ line: l, seed: true })}\n\n`));
  } catch {}
  const seedTasks = loadTasks().map(t => ({
    ...t,
    status: getTaskStatus((t.targets || [t.role])[0], t.id),
  }));
  const workflow = { ...loadWorkflowState(), reviewQueue: getReviewQueue() };
  res.write(`data: ${JSON.stringify({ tasks: seedTasks, workflow, seed: true })}\n\n`);

  const tick = setInterval(() => {
    try {
      const stat = fs.statSync(logFile);
      if (stat.size > lastSize) {
        const fd  = fs.openSync(logFile, 'r');
        const buf = Buffer.alloc(stat.size - lastSize);
        fs.readSync(fd, buf, 0, buf.length, lastSize);
        fs.closeSync(fd);
        lastSize = stat.size;
        buf.toString().split('\n').filter(Boolean)
           .forEach(l => res.write(`data: ${JSON.stringify({ line: l })}\n\n`));
      }
    } catch {}
    const tasks = loadTasks().map(t => ({
      ...t,
      status: getTaskStatus((t.targets || [t.role])[0], t.id),
    }));
    const workflow = { ...loadWorkflowState(), reviewQueue: getReviewQueue() };
    res.write(`data: ${JSON.stringify({ inbox: getInboxCounts(), tmux: checkTmux(), tasks, workflow, active: getActiveAgents() })}\n\n`);
  }, 1500);

  req.on('close', () => clearInterval(tick));
});

