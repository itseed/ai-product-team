function parseConfig() {
  try {
    const content = fs.readFileSync(CONFIG_FILE, 'utf8');
    const cfg = {};
    const re  = /^(AI_\w+)="(\w+)"/gm;
    let m;
    while ((m = re.exec(content)) !== null) cfg[m[1]] = m[2];
    return cfg;
  } catch { return {}; }
}

// Allowed AI values (prevents shell injection)
const ALLOWED_AI_VALUES = ['claude', 'gemini', 'cursor'];
const ALLOWED_CONFIG_KEYS = ['AI_PO', 'AI_TECH', 'AI_DEV', 'AI_QA', 'AI_DEVOPS'];

function updateConfig(updates) {
  for (const [k, v] of Object.entries(updates)) {
    if (!ALLOWED_CONFIG_KEYS.includes(k))
      throw new Error(`Invalid config key: ${k}`);
    const val = String(v).toLowerCase().trim();
    if (!ALLOWED_AI_VALUES.includes(val))
      throw new Error(`Invalid AI value for ${k}: must be one of ${ALLOWED_AI_VALUES.join(', ')}`);
  }
  let content = fs.readFileSync(CONFIG_FILE, 'utf8');
  for (const [k, v] of Object.entries(updates)) {
    const val = String(v).toLowerCase().trim();
    content = content.replace(
      new RegExp(`^(${k}=")[^"]*(".*$)`, 'm'),
      `$1${val}$2`
    );
  }
  fs.writeFileSync(CONFIG_FILE, content);
}

function getInboxCounts() {
  const roles = ['po', 'tech', 'dev', 'qa', 'devops'];
  return Object.fromEntries(roles.map(r => {
    try {
      const n = fs.readdirSync(path.join(INBOX_DIR, r))
                  .filter(f => f.endsWith('.task')).length;
      return [r, n];
    } catch { return [r, 0]; }
  }));
}

function checkTmux() {
  try { execSync('tmux has-session -t ai_team 2>/dev/null'); return true; }
  catch { return false; }
}

function sendTask(role, sender, task, opts = {}) {
  const dir = path.join(INBOX_DIR, role);
  fs.mkdirSync(dir, { recursive: true });
  const id = opts.taskId || Date.now();
  const lines = [`FROM: ${sender}`, `TIME: ${new Date().toISOString()}`, `TASK: ${task}`];
  if (opts.workflowId) lines.push(`WORKFLOW_ID: ${opts.workflowId}`);
  if (opts.handoffTo) lines.push(`HANDOFF_TO: ${opts.handoffTo}`);
  const file = path.join(dir, `${id}.task`);
  fs.writeFileSync(file, lines.join('\n') + '\n');
  return id;
}

function loadTasks() {
  try {
    const raw = fs.readFileSync(TASKS_FILE, 'utf8');
    return JSON.parse(raw);
  } catch { return []; }
}

function saveTask(task) {
  const tasks = loadTasks();
  tasks.unshift(task);
  const trimmed = tasks.slice(0, 50);
  fs.mkdirSync(path.dirname(TASKS_FILE), { recursive: true });
  fs.writeFileSync(TASKS_FILE, JSON.stringify(trimmed, null, 2));
}

function getTaskStatus(role, taskId) {
  const taskFile = path.join(INBOX_DIR, role, `${taskId}.task`);
  const doneFile = path.join(INBOX_DIR, role, `${taskId}.done`);
  if (fs.existsSync(taskFile)) return 'queued';
  if (fs.existsSync(doneFile)) return 'done';
  return 'done';
}

// Detect agents actively working: .done file modified within last 2 min
function getActiveAgents() {
  const roles = ['po', 'tech', 'dev', 'qa', 'devops'];
  const now = Date.now();
  const WINDOW_MS = 120_000;
  return Object.fromEntries(roles.map(r => {
    try {
      const dir = path.join(INBOX_DIR, r);
      const active = fs.readdirSync(dir)
        .filter(f => f.endsWith('.done'))
        .some(f => now - fs.statSync(path.join(dir, f)).mtimeMs < WINDOW_MS);
      return [r, active];
    } catch { return [r, false]; }
  }));
}

function buildFileTree(dir, base = '') {
  const items = [];
  try {
    for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
      const rel = base ? `${base}/${e.name}` : e.name;
      if (e.isDirectory()) {
        items.push({ name: e.name, path: rel, type: 'dir',
                     children: buildFileTree(path.join(dir, e.name), rel) });
      } else {
        const { size } = fs.statSync(path.join(dir, e.name));
        items.push({ name: e.name, path: rel, type: 'file', size });
      }
    }
  } catch {}
  return items;
}

function dirExists(p) {
  try { return fs.statSync(p).isDirectory(); }
  catch { return false; }
}

